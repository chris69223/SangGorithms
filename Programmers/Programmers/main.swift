import Foundation

func solution(_ n:Int64) -> [Int] {
    //어캐 푸냐 생각하면?
    var t = String(n)
    let arr:[Character] = []
    for i in t{
        arr.append[i]
    }
    // let arr:[Int] = []
    return Int64(arr.reversed())
}
